function Message1(k,timeInstr)
CellText = {'Our aim is to concentrate',...
            'on Necker cube perception.',...
            'In the cube center,',...
            'the red spot will be shown',...
            'on which you should concentrate.',...
            ' ',...
            ' ',...
            'So, we start in a few seconds...',...
            };
cgalign('c','c'); cgpencol([1 1 1]); cgfont('Arial',fix(30*k));

del = 40; tempR = 200;

for i=1:length(CellText)
cgtext(CellText{i},0,fix(tempR*k));
tempR = tempR-del;
end
cgflip([0 0 0]);
pause(timeInstr);